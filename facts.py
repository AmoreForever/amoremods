from .. import loader, utils
from telethon import functions
from asyncio import sleep
import random
import datetime
chat = "@faktiru"


class FactsMod(loader.Module):
    """More Interesting Facts"""

    strings = {
        "name": "Facts",
        "wait": "<emoji document_id=5472146462362048818>üí°</emoji> Searching..."
    }

    strings_ru = {
        "wait": "<emoji document_id=5472146462362048818>üí°</emoji> –ü–æ–∏—Å–∫..."
    }

    @loader.command(ru_docs="–ò–Ω—Ç–µ—Ä–µ—Å–Ω—ã–µ –§–∞–∫—Ç—ã")
    async def afactscmd(self, message):
        """Intersting Facts"""
        reply = await message.get_reply_message()
        await utils.answer(message, self.strings["wait"])
        result = await message.client(
            functions.messages.GetHistoryRequest(
                peer=chat, offset_id=0, offset_date=datetime.datetime.now(), add_offset=random.randint(0, 1000), limit=1, max_id=0, min_id=0, hash=0,
            )
        )
        await sleep(0.30)
        await message.delete()
        await message.client.send_message(
            message.to_id,
            result.messages[0],
            reply_to=reply.id if reply else None,
        )
