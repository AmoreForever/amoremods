from .. import loader, utils
import datetime
from time import strftime


@loader.tds
class NYMod(loader.Module):
    """Check how much is left until the new year"""

    strings = {'name': 'NewYearTimer'}

    async def nycmd(self, message):
        """Check date"""
        now = datetime.datetime.now()
        ng = datetime.datetime(int(strftime('%Y')) + 1, 1, 1)
        d = ng - now
        mm, ss = divmod(d.seconds, 60)
        hh, mm = divmod(mm, 60)
        soon = f'<b><emoji document_id=6334530007968253960>‚òÉÔ∏è</emoji> Until the <u>New Year</u>: {d.days} d. {hh} h. {mm} m. {ss} s.</b>\n<b><emoji document_id=5393226077520798225>ü•∞</emoji> Wait for the new year together <u>Family</u></b>'
        await utils.answer(message, soon)
